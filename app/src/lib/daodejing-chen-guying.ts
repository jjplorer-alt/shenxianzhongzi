function stripTrailingYinshu(raw: string): string {
  const markers = [
    /【\s*引述[】\}]?/,
    /【\s*51\s*述[】\}]?/,
    /\[\s*51\s*述/,
    /\(51述/,
    /【吉\|述/,
    /法霖、今海与引述/,
    /注释、今译与引述/,
    /注[辑糖]*、今/,
  ];
  let s = raw;
  for (const re of markers) {
    const m = s.search(re);
    if (m !== -1) s = s.slice(0, m);
  }
  return s.trim();
}

function lightFixOcr(s: string): string {
  return s
    .replace(/^元，是形成天地的本始/, "无，是形成天地的本始")
    .replace(/去观照遵的端倪/, "去观照道的端倪")
    .replace(/然商/g, "然而")
    .replace(/承不停竭/g, "永不停竭")
    .replace(/空虚假不会穷竭/g, "空虚而不会穷竭")
    .replace(/\. /g, "。")
    .replace(/o$/g, "。");
}

