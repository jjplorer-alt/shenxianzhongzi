名声和身体哪个更亲近？身体和财物哪个更重要？得到和失去哪个更有害？过分的爱惜必定会造成更大的耗费；过多的收藏必定会招致更惨重的损失。所以知道满足就不会受到屈辱，知道适可而止就不会遇到危险，可以长久平安。
最完美的东西好像有欠缺，但它的作用不会衰竭。最充盈的东西好像空虚，但它的作用不会穷尽。最正直的好像弯曲，最灵巧的好像笨拙，最善辩的好像口齿迟钝。安静胜过躁动，寒冷胜过炎热。清静无为是天下的正道。
天下有道的时候，战马都用来耕田。天下无道的时候，怀胎的母马也要在战场上生驹。最大的祸患莫过于不知足，最大的罪过莫过于贪得无厌。所以知道满足的这种满足，是永远的满足。
不出家门，就能知道天下的事情；不看窗外，就能了解自然的规律。走得越远，知道的越少。因此圣人不用出行就能知道，不用看见就能明了，不用作为就能成功。
追求学问，知识一天比一天增加；追求道，欲望一天比一天减少。减少又减少，直到达到无为的境界。无为就没有什么事情做不成。治理天下要经常保持清静无事，如果政令繁多，就不足以治理天下了。
圣人没有自己的私心，以百姓的心为心。善良的人，我善待他；不善良的人，我也善待他；这样就得到了善。诚信的人，我信任他；不诚信的人，我也信任他；这样就得到了诚信。圣人治理天下，收敛自己的意欲，使天下人的心思归于浑朴。百姓都专注于自己的耳目，圣人都把他们当作婴儿一样看待。
人从出生到死亡。长寿的人，占十分之三；短命的人，占十分之三；人本来可以长寿，却自己走向死亡的，也占十分之三。这是什么原因呢？因为他们太过于保养生命了。听说善于养生的人，在陆地上行走不会遇到犀牛和老虎，进入战场不会被兵器伤害；犀牛没有地方用它的角，老虎没有地方用它的爪，兵器没有地方用它的刃。这是什么原因呢？因为他没有进入死亡的境地。
道产生万物，德养育万物，物质使万物成形，环境使万物成长。因此万物没有不尊崇道而珍贵德的。道之所以被尊崇，德之所以被珍贵，是因为它们不发号施令，而是顺其自然。所以道产生万物，德养育万物；使万物生长发育，使万物成熟结果，使万物得到养护和庇护。生长了不占有，作为了不自恃，引领了不主宰，这叫做深远的德。
天下万物都有开端，这个开端是天下万物的本源。既然知道了本源，就能认识万物；既然认识了万物，又能坚守本源，终身都不会有危险。堵塞感官的孔窍，关闭欲望的门户，终身都不会劳苦。打开感官的孔窍，去成就自己的事业，终身都不可救药。能看见细微的叫做明，能坚守柔弱的叫做强。运用智慧的光芒，返回到内在的明察，不给自己留下灾祸；这就是遵循永恒的规律。
假如我稍微有点知识，行走在大道上，就只怕走上邪路。大道很平坦，可是人们却喜欢走小路。朝廷很腐败，农田很荒芜，仓库很空虚；却穿着华丽的衣服，佩带着锋利的宝剑，饱吃精美的饮食，占有多余的财货；这叫做强盗头子。这是多么不符合道啊！
善于建立的不会被拔除，善于抱持的不会脱落，子孙因此世代祭祀不断。用道来修养自身，他的德就会真实；用道来修养家庭，他的德就会有余；用道来修养乡里，他的德就会增长；用道来修养国家，他的德就会丰盛；用道来修养天下，他的德就会普遍。所以从自身观察他人，从自家观察他家，从本乡观察他乡，从本国观察他国，从天下观察天下。我怎么知道天下的情况呢？就是用这个方法。
德行深厚的人，好比初生的婴儿。毒虫不蜇他，猛兽不伤害他，凶鸟不捕捉他。他筋骨柔弱但拳头握得很紧。不知道男女交合但小生殖器却能勃起，这是精气充足的缘故。整天啼哭但嗓子不沙哑，这是和谐的缘故。知道和谐叫做常，知道常叫做明。贪生纵欲叫做不祥。用心智驱使精气叫做逞强。事物过于强壮就会走向衰老，这叫做不符合道，不符合道就会很快灭亡。
真正有智慧的人不说话，说话的人没有真正的智慧。堵塞感官的孔窍，关闭欲望的门户，消磨锋芒，化解纷扰，调和光辉，混同尘垢，这叫做“玄同”（深奥的同一）。所以对他无法亲近，也无法疏远；无法给他利益，也无法伤害他；无法使他尊贵，也无法使他卑贱。因此被天下人所尊贵。
用正道治理国家，用奇术用兵打仗，用清静无为夺取天下。我怎么知道是这样的呢？根据是：天下的禁忌越多，人民就越贫穷；人们的武器越多，国家就越混乱；人们的技巧越多，奇怪的东西就越层出不穷；法令越严明，盗贼就越多。所以圣人说：“我无为，人民就自然化育；我好静，人民就自然端正；我无事，人民就自然富足；我无欲，人民就自然质朴。”
政治宽厚，人民就淳朴；政治严苛，人民就狡黠。灾祸啊，幸福就倚傍在它旁边；幸福啊，灾祸就隐藏在它里面。谁知道它们的终极呢？它们没有一个确定的标准。正可以转化为邪，善可以转化为恶。人们的迷惑，已经有很长时间了。因此圣人方正而不生硬，锐利而不伤人，直率而不放肆，光亮而不耀眼。
治理人民，侍奉上天，没有比节俭更重要的了。只有节俭，才能早做准备；早做准备就是不断积累德；不断积累德就没有什么不能战胜的；没有什么不能战胜的就无法估量他的力量；无法估量他的力量就可以拥有国家；掌握了治理国家的根本，就可以长久。这叫做根深蒂固，长生久视的道理。
治理大国，就像煎小鱼一样。用道来治理天下，鬼神就不灵验了；不是鬼神不灵验，而是它们的神灵不伤害人；不仅神灵不伤害人，圣人也不伤害人。两者都不伤害人，所以德就都归向人民了。
大国要像江河的下游一样，处于天下雌柔的位置，是天下交汇的地方。雌柔常常以安静胜过雄强，因为安静而处于低下的位置。所以大国对小国谦下，就能取得小国的归附；小国对大国谦下，就能取得大国的容纳。所以有的谦下取得归附，有的谦下取得容纳。大国不过是想兼并养育小国，小国不过是想侍奉大国。两者都能满足自己的愿望，大国更应该谦下。
道是万物的主宰。是善人的珍宝，也是不善人的依靠。美好的言辞可以换来尊敬，美好的行为可以影响他人。人即使不善，又怎么能抛弃道呢？所以拥立天子，设置三公，虽然有捧着玉璧在先、驷马在后的隆重礼仪，不如献上道这个珍宝。古代之所以重视道，是什么原因呢？不就是说：有求就能得到，有罪就能免除吗？所以被天下人所尊贵。
以无为的态度去作为，以无事的态度去做事，以无味的态度去品味。把小看作大，把少看作多，用恩德来报答怨恨。解决困难要从容易的地方入手，成就大事要从细微的地方做起；天下的难事，必定从容易的开始；天下的大事，必定从细微的开始。因此圣人始终不做大事，所以才能成就大事。轻易许诺必定很少守信用，把事情看得太容易必定会遇到很多困难。因此圣人总是把事情看得很难，所以最终没有困难。
事物安稳的时候容易把握，事物还没有显露迹象的时候容易谋划。事物脆弱的时候容易分解，事物微小的时候容易消散。要在事情还没有发生的时候就做好准备，要在祸乱还没有形成的时候就进行治理。合抱的大树，生长于细小的萌芽；九层的高台，筑起于每一堆泥土；千里的远行，开始于脚下的第一步。强行去做就会失败，执着去掌控就会失去。因此圣人无为所以没有失败，不执着所以没有失去。人们做事，常常在快要成功的时候失败。如果在事情结束时也像开始时那样谨慎，就不会有失败的事情。因此圣人追求别人所不追求的，不看重难得的财物；学习别人所不学习的，补救众人所犯的过错。以此辅助万物的自然发展，而不敢强行去做。
古代善于行道的人，不是使人民聪明，而是使人民质朴。人民之所以难以治理，是因为他们的智巧太多。所以用智巧治理国家，是国家的灾祸；不用智巧治理国家，是国家的福气。知道这两点就是法则。永远记住这个法则，这叫做深远的德。深远的德啊，深奥啊，遥远啊，与万物一起回归本源，然后才能达到最大的顺随自然。
江海之所以能成为百川汇集的地方，是因为它善于处在低下的位置，所以能成为百川之王。因此圣人想要统治人民，必须用言辞对人民表示谦下；想要领导人民，必须把自己的利益放在人民的后面。因此圣人处在上面而人民不感到负担，处在前面而人民不感到妨害。因此天下人都乐意拥戴他而不感到厌烦。因为他不与人争，所以天下没有人能和他争。
天下人都说我的道很大，好像不像任何具体的东西。正因为它很大，所以才不像任何具体的东西。如果像具体的东西，早就变得渺小了！我有三件宝贝，我持有并保全它们。第一件叫做慈爱，第二件叫做节俭，第三件叫做不敢处在天下人的前面。因为慈爱，所以能勇敢；因为节俭，所以能宽广；因为不敢处在天下人的前面，所以能成为万物的首长。现在舍弃慈爱而追求勇敢，舍弃节俭而追求宽广，舍弃退让而追求争先，那是死路一条！慈爱，用来打仗就能胜利，用来防守就能坚固。上天要救助谁，就用慈爱来保护谁。
善于做将帅的人，不逞勇武；善于打仗的人，不轻易发怒；善于战胜敌人的人，不与敌人正面交锋；善于用人的人，对人表示谦下。这叫做不争的德，这叫做运用别人的力量，这叫做符合自然的最高准则。
用兵的人说过：“我不敢主动进攻，而采取守势；不敢前进一寸，而宁愿后退一尺。”这叫做虽然有阵势，却像没有阵势一样；虽然要挥臂，却像没有手臂一样；虽然要面对敌人，却像没有敌人一样；虽然拿着兵器，却像没有兵器一样。最大的祸患莫过于轻敌，轻敌几乎丧失了我的宝贝。所以两军实力相当的时候，悲愤的一方会获得胜利。
我的话很容易理解，很容易实行。可是天下没有人能理解，没有人能实行。言论有宗旨，做事有根本。正因为人们不了解这个道理，所以不了解我。了解我的人很少，效法我的人就更可贵了。因此圣人穿着粗布衣服，怀里却揣着美玉。
知道自己有所不知，这是最好的；不知道却自以为知道，这是毛病。圣人没有这个毛病，因为他把这个毛病当作毛病。正因为把这个毛病当作毛病，所以才没有这个毛病。
人民不畏惧统治者的威压，那么更大的灾祸就要到来了。不要逼迫人民的居所，不要压榨人民的生活。只有不压榨人民，人民才不会厌恶统治者。因此圣人有自知之明而不自我表现，懂得自爱而不自以为高贵。所以舍弃后者，选取前者。
