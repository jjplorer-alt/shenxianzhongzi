人活着的时候身体是柔软的，死了以后就变得僵硬了。草木活着的时候是柔软脆弱的，死了以后就变得干枯了。所以坚强的东西属于死亡的一类，柔弱的东西属于生存的一类。因此军队强大了就会灭亡，树木强大了就会折断。强大的处于下位，柔弱的处于上位。
